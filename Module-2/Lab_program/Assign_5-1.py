fruits = ("Apple", "Banana", "Cherry", "Mango", "Orange")

print("Tuple:", fruits)

print("\nAccessing elements using positive indexing:")
print("Element at index 0:", fruits[0])
print("Element at index 2:", fruits[2])
print("Element at index 4:", fruits[4])

print("\nAccessing elements using negative indexing:")
print("Last element :", fruits[-1])
print("Second last element :", fruits[-2])
print("Third last element :", fruits[-3])
