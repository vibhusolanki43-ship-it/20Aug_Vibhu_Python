fruits = ("Apple", "Banana", "Cherry", "Mango", "Orange", "Grapes")

print("Original tuple:", fruits)

print("Slice from index 1 to 3:", fruits[1:4])

print("Slice from start to index 2:", fruits[:3])

print("Slice from index 3 to end:", fruits[3:])

print("Every second element:", fruits[::2])

print("Tuple in reverse order:", fruits[::-1])
